students = {"Bob":12, "Rachel":13, "Emily":15}

print(students)

print(students["Bob"])

# Change a value of a dictionary
students["Rachel"] = 15

print(students["Rachel"])

# Delete a value in dictionary by a key
del students["Emily"]

print(students)

# Add to dictionary
students["Rody"] = 70;
students["Manny"] = 24;

print(students)